namespace Class_And_Methods_Assignment
{
    public class MathOperations
    {
        public int Add(int input)
        {
            return input + input;
        }

        public int Multiply (int input)
        {
            return input * input;
        }

        public int Subtract(int input)
        {
            return input*3 - input;
        }

    }
